def div (n):
    if n % 2 == 0:
       print (f"{n}is divisible by 2")
    

    elif  n % 3 == 0:
         print (f"{n}is divisible by 3 ")
    elif n % 5 == 0:
        print(f"{n}is divisible by 5")     

    else:
        print(f"{n}is not divisible") 
        
